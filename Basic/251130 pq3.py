import heapq

arr = [5,2,8,1,9]

heap = []

for num in arr:
    priority = 0 if num % 2 == 0 else 1
    heapq.heappush(heap, (priority, num))
    
sorted_arr = []
while heap:
    _, num = heapq.heappop(heap)
    sorted_arr.append(num)
    
print(sorted_arr)

